package Model;

import java.util.List;

/**
 *
 * @author Bárbara
 */
public class Uasg {

    private boolean ativo; //Se a UASG está ativa.
    private String cep; //CEP da UASG.
    private int ddd; //DDD do telefone da UASG.
    private String endereco; //Descrição do endereço da UASG.
    private long fax; //Fax da UASG
    private int id; //Identificador único da UASG no SICAF.
    private int id_municipio; //Identificador único do município da UASG
    private int co_orgao; //Identificador único do órgão no SICAF.
    private String no_uasg; //Nome da UASG.
    private String no_mnemonico; //Nome mnemônico da UASG.
    private int ramal; //Ramal do telefone da UASG.
    private int ramal2; //Ramal do telefone alternativo da UASG.
    private String uf; //Sigla da unidade federativa
    private long telefone; //Telefone da UASG.
    private long telefone2; //Telefone alternativo da UASG.
    private int total_fornecedores_cadastrados; //Quantidade total de fornecedores cadastrados na UASG.
    private int total_fornecedores_recadastrados; //Quantidade total de fornecedores recadastrados, no novo SICAF, na UASG.
    private boolean unidade_cadastradora; //Unidade cadastradora.
    private List<Compra> compras;

    public Uasg() {
        super();
    }

    public Uasg(boolean ativo, String cep, int ddd, String endereco, long fax, int id, int id_municipio, int co_orgao, String no_uasg, String no_mnemonico, int ramal, int ramal2, String uf, long telefone, long telefone2, int total_fornecedores_cadastrados, int total_fornecedores_recadastrados, boolean unidade_cadastradora, List<Compra> compras) {
        this.ativo = ativo;
        this.cep = cep;
        this.ddd = ddd;
        this.endereco = endereco;
        this.fax = fax;
        this.id = id;
        this.id_municipio = id_municipio;
        this.co_orgao = co_orgao;
        this.no_uasg = no_uasg;
        this.no_mnemonico = no_mnemonico;
        this.ramal = ramal;
        this.ramal2 = ramal2;
        this.uf = uf;
        this.telefone = telefone;
        this.telefone2 = telefone2;
        this.total_fornecedores_cadastrados = total_fornecedores_cadastrados;
        this.total_fornecedores_recadastrados = total_fornecedores_recadastrados;
        this.unidade_cadastradora = unidade_cadastradora;
        this.compras = compras;
    }

    public boolean isAtivo() {
        return ativo;
    }

    public void setAtivo(boolean ativo) {
        this.ativo = ativo;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public int getDdd() {
        return ddd;
    }

    public void setDdd(int ddd) {
        this.ddd = ddd;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public long getFax() {
        return fax;
    }

    public void setFax(long fax) {
        this.fax = fax;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_municipio() {
        return id_municipio;
    }

    public void setId_municipio(int id_municipio) {
        this.id_municipio = id_municipio;
    }

    public int getCo_orgao() {
        return co_orgao;
    }

    public void setCo_orgao(int co_orgao) {
        this.co_orgao = co_orgao;
    }

    public String getNo_uasg() {
        return no_uasg;
    }

    public void setNo_uasg(String no_uasg) {
        this.no_uasg = no_uasg;
    }

    public String getNo_mnemonico() {
        return no_mnemonico;
    }

    public void setNo_mnemonico(String no_mnemonico) {
        this.no_mnemonico = no_mnemonico;
    }

    public int getRamal() {
        return ramal;
    }

    public void setRamal(int ramal) {
        this.ramal = ramal;
    }

    public int getRamal2() {
        return ramal2;
    }

    public void setRamal2(int ramal2) {
        this.ramal2 = ramal2;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public long getTelefone() {
        return telefone;
    }

    public void setTelefone(long telefone) {
        this.telefone = telefone;
    }

    public long getTelefone2() {
        return telefone2;
    }

    public void setTelefone2(long telefone2) {
        this.telefone2 = telefone2;
    }

    public int getTotal_fornecedores_cadastrados() {
        return total_fornecedores_cadastrados;
    }

    public void setTotal_fornecedores_cadastrados(int total_fornecedores_cadastrados) {
        this.total_fornecedores_cadastrados = total_fornecedores_cadastrados;
    }

    public int getTotal_fornecedores_recadastrados() {
        return total_fornecedores_recadastrados;
    }

    public void setTotal_fornecedores_recadastrados(int total_fornecedores_recadastrados) {
        this.total_fornecedores_recadastrados = total_fornecedores_recadastrados;
    }

    public boolean isUnidade_cadastradora() {
        return unidade_cadastradora;
    }

    public void setUnidade_cadastradora(boolean unidade_cadastradora) {
        this.unidade_cadastradora = unidade_cadastradora;
    }

    public List<Compra> getCompras() {
        return compras;
    }

    public void setCompras(List<Compra> compras) {
        this.compras = compras;
    }

}
