package Model;

import java.util.Date;

/**
 *
 * @author Bárbara
 */
public class Contrato {
    private Object id_compra; //id do MongoDB que referencia compra.
    private String cnpj_contratada; //CNPJ da empresa contratada.
    private String cpf_contratada; //CPF da contratada.
    private Date data_assinatura; //Data de assinatura do contrato.
    private Date data_inicio_vigencia; //Data de início de vigência dos contratos.
    private Date data_termino_vigencia; //Data de término de vigência dos contratos.
    private String fundamento_legal; //Fundamento legal do processo de contratação.
    private long identificador; //Identificador do Contrato
    private long licitacao_associada; //Referência à licitação que originou a contratação.
    private int modalidade_licitacao; //Número e o ano da licitação que originou a contratação.
    private int numero; //Campo seguido pelo número do contrato, seguido do respectivo ano.
    private int numero_aditivo; //Quantidade de termos aditivos de um contrato.
    private int numero_licitacao; //Número do aviso da licitação que originou a contratação.
    private long numero_processo; //Número do processo de contratação.
    private String objeto; //Descrição do objeto, a partir de uma descrição de item/serviço informada.
    private String origem_licitacao; //Origem da licitação que gerou o contrato: Preço praticado(SISPP) ou Registro de preço(SISRP).
    private int tipo_contrato; //Tipo de Contrato.
    private int uasg; //Campo de seis digitos que indica o código da UASG contratante.
    private Double vl_inicial; //Valor inicial do contrato.

    public Contrato() {
        super();
    }

    public Contrato(Object id_compra, String cnpj_contratada, String cpf_contratada, Date data_assinatura, Date data_inicio_vigencia, Date data_termino_vigencia, String fundamento_legal, long identificador, long licitacao_associada, int modalidade_licitacao, int numero, int numero_aditivo, int numero_licitacao, long numero_processo, String objeto, String origem_licitacao, int tipo_contrato, int uasg, Double valor_inicial) {
        this.id_compra = id_compra;
        this.cnpj_contratada = cnpj_contratada;
        this.cpf_contratada = cpf_contratada;
        this.data_assinatura = data_assinatura;
        this.data_inicio_vigencia = data_inicio_vigencia;
        this.data_termino_vigencia = data_termino_vigencia;
        this.fundamento_legal = fundamento_legal;
        this.identificador = identificador;
        this.licitacao_associada = licitacao_associada;
        this.modalidade_licitacao = modalidade_licitacao;
        this.numero = numero;
        this.numero_aditivo = numero_aditivo;
        this.numero_licitacao = numero_licitacao;
        this.numero_processo = numero_processo;
        this.objeto = objeto;
        this.origem_licitacao = origem_licitacao;
        this.tipo_contrato = tipo_contrato;
        this.uasg = uasg;
        this.vl_inicial = valor_inicial;
    }

    public Object getId_compra() {
        return id_compra;
    }

    public void setId_compra(Object id_compra) {
        this.id_compra = id_compra;
    }
    
    public String getCnpj_contratada() {
        return cnpj_contratada;
    }

    public void setCnpj_contratada(String cnpj_contratada) {
        this.cnpj_contratada = cnpj_contratada;
    }

    public String getCpf_contratada() {
        return cpf_contratada;
    }

    public void setCpf_contratada(String cpf_contratada) {
        this.cpf_contratada = cpf_contratada;
    }

    public Date getData_assinatura() {
        return data_assinatura;
    }

    public void setData_assinatura(Date data_assinatura) {
        this.data_assinatura = data_assinatura;
    }

    public Date getData_inicio_vigencia() {
        return data_inicio_vigencia;
    }

    public void setData_inicio_vigencia(Date data_inicio_vigencia) {
        this.data_inicio_vigencia = data_inicio_vigencia;
    }

    public Date getData_termino_vigencia() {
        return data_termino_vigencia;
    }

    public void setData_termino_vigencia(Date data_termino_vigencia) {
        this.data_termino_vigencia = data_termino_vigencia;
    }

    public String getFundamento_legal() {
        return fundamento_legal;
    }

    public void setFundamento_legal(String fundamento_legal) {
        this.fundamento_legal = fundamento_legal;
    }

    public long getIdentificador() {
        return identificador;
    }

    public void setIdentificador(long identificador) {
        this.identificador = identificador;
    }

    public long getLicitacao_associada() {
        return licitacao_associada;
    }

    public void setLicitacao_associada(long licitacao_associada) {
        this.licitacao_associada = licitacao_associada;
    }

    public int getModalidade_licitacao() {
        return modalidade_licitacao;
    }

    public void setModalidade_licitacao(int modalidade_licitacao) {
        this.modalidade_licitacao = modalidade_licitacao;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getNumero_aditivo() {
        return numero_aditivo;
    }

    public void setNumero_aditivo(int numero_aditivo) {
        this.numero_aditivo = numero_aditivo;
    }

    public int getNumero_licitacao() {
        return numero_licitacao;
    }

    public void setNumero_licitacao(int numero_licitacao) {
        this.numero_licitacao = numero_licitacao;
    }

    public long getNumero_processo() {
        return numero_processo;
    }

    public void setNumero_processo(long numero_processo) {
        this.numero_processo = numero_processo;
    }

    public String getObjeto() {
        return objeto;
    }

    public void setObjeto(String objeto) {
        this.objeto = objeto;
    }

    public String getOrigem_licitacao() {
        return origem_licitacao;
    }

    public void setOrigem_licitacao(String origem_licitacao) {
        this.origem_licitacao = origem_licitacao;
    }

    public int getTipo_contrato() {
        return tipo_contrato;
    }

    public void setTipo_contrato(int tipo_contrato) {
        this.tipo_contrato = tipo_contrato;
    }

    public int getUasg() {
        return uasg;
    }

    public void setUasg(int uasg) {
        this.uasg = uasg;
    }

    public Double getValor_inicial() {
        return vl_inicial;
    }

    public void setValor_inicial(Double valor_inicial) {
        this.vl_inicial = valor_inicial;
    }

}
